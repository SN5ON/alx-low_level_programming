#include <stdio.h>

/**
 * add - add two ints
 * @a: first int
 * @b: second int
 * Return: addition
 */
int add(int a, int b)
{
	return (a + b);
}


/**
 * sub - subtract two ints
 * @a: first int
 * @b: second int
 * Return: sum of subtraction
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * div - divided two ints
 * @a: first int
 * @b: second int
 * Return: total sum of division
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - mod two ints
 * @a: first int
 * @b: second int
 * Return: sum of mod
 */
int mod(int a, int b)
{
	return (a % b);
}

/**
 * mul - multiply two ints
 * @a: first int
 * @b: second int
 * Return: total multiplication
 */
int mul(int a, int b)
{
	return (a * b);
}
